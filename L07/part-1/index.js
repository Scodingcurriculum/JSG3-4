//avaScript has different types of data that it can work with. Think of them as different kinds of containers that hold different things.
//There are two types of data types in JavaScript.
//1- Primitive data type
//2- Non-primitive (reference) data type


console.log("Primitive datatypes")
// Numbers: Numbers are used for counting or measuring things.
console.log("Example of Number Data Type")
let candies = 10; // This container holds the number 10 (candies)
let buildingHeight = 50; // This container holds the number 50 (buildingHeight)

console.log(candies); // Output: 10
console.log(buildingHeight); // Output: 50

console.log("\n")

console.log("Example of String Data Type")
// Strings: Strings are used for text or words.
let name = "Alice"; // This container holds the word "Alice" (name)
let message = "Hello, world!"; // This container holds the sentence "Hello, world!" (message)

console.log(name); // Output: Alice
console.log(message); // Output: Hello, world!

console.log("\n")

console.log("Example of Boolean Data Type")
// Booleans: Booleans are used for true/false or yes/no values.
let isRaining = true; // This container holds the value "true" for 'isRaining'
let isSunny = false; // This container holds the value "false" for 'isSunny'

console.log(isRaining); // Output: true
console.log(isSunny); // Output: false

console.log("\n")


console.log("Non-primitive datatypes")
console.log("Example of Arrays")
// Arrays: Arrays are used for lists of items.
let toys = ["teddy bear", "lego", "doll"]; // This container holds a list of toys (toys)
let fruits = ["apple", "banana", "orange"]; // This container holds a list of fruits (fruits)

console.log(toys); // Output: ["teddy bear", "lego", "doll"]
console.log(fruits); // Output: ["apple", "banana", "orange"]

console.log("\n")

console.log("Example of Objects")
// Objects: Objects are used for storing related data in key-value pairs.
let person = {
  name: "Bob",
  age: 10,
  isStudent: true
}; // This container holds different information about a person (person)

console.log(person); // Output: { name: "Bob", age: 10, isStudent: true }
