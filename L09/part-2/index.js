//Program to generate a random number.

// Generate a random number between 1 and 10
let randomNumber = Math.floor(Math.random() * 10) + 1;

// Display the generated random number
console.log("Random number: " + randomNumber);

//This code uses Math.random() to generate a random decimal between 0 (inclusive) and 1 (exclusive). By multiplying this random decimal by 10 and then using Math.floor(), it gets an integer value between 0 and 9. Finally, adding 1 to this result creates a random number between 1 and 10.
