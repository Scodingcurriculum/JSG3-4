// Program that converts a decimal number to its binary equivalent

let decimal = 15; // Decimal number to convert
let binary = decimal.toString(2); // Convert decimal to binary using toString()

console.log(`Binary representation of ${decimal}: ${binary}`);
// Output: Binary representation of 15: 1111

//let decimal = 15;: Defines the decimal number to convert (you can change this number).
//decimal.toString(2);: Utilizes the toString() method with base 2 as an argument to convert the decimal number to its binary representation directly.
